#!/usr/bin/python3

print()
#while loop
i = 1
while i < 6:
  print(i)
  i += 1
print()

#while with else
i = 1
while i < 6:
  print(i)
  i += 1
else:
    print("Finally while loop finished..!")
print()

#For loop
fruits = ["apple", "banana", "cherry"]
for x in fruits:
  print(x)
print()

#for with else
fruits = ["apple", "banana", "cherry"]
for x in fruits:
  print(x)
else:
    print("Finally for loop finished..!")
print()

#Loop through string
for x in "banana":
  print(x)
print()
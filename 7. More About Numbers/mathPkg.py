#!/usr/bin/python3

print()

#import statement
import math

# PI constant
print("PI Constant:", math.pi)
print()

# E constant
print("E Constant:", math.e)
print()

#abs function majors distance from 0 to provided number
print("abs(-10): ", abs(-10))
print("abs(20): ", abs(20))
print()

#ceil - This method returns the smallest integer not less than x.
print ("math.ceil(-5.17) : ", math.ceil(-5.137))
print ("math.ceil(10.12) : ", math.ceil(10.132))
print ("math.ceil(100) : ", math.ceil(100))
print()

# exp() method returns exponential of x: ex. e^x
print ("math.exp(-45.17) : ", math.exp(-45.17))
print ("math.exp(100.12) : ", math.exp(100.12))
print ("math.exp(100.72) : ", math.exp(100.72))
print ("math.exp(math.pi) : ", math.exp(math.pi))
print()

# method returns the floor of x i.e. the largest integer not greater than x
print ("math.floor(-80.17) : ", math.floor(-80.17))
print ("math.floor(10.12) : ", math.floor(10.12))
print ("math.floor(110.72) : ", math.floor(110.72))
print()

